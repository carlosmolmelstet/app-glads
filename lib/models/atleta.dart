class Atleta {
  int id;
  String nome;
  String? avatar;
  String cpf;
  String telefone;
  String email;
  String endereco;

  Atleta({
    required this.id,
    required this.nome,
    required this.avatar,
    required this.cpf,
    required this.telefone,
    required this.email,
    required this.endereco,
  });
}
